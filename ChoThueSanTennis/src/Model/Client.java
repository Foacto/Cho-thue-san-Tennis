package Model;

import java.io.Serializable;

/**
 *
 * @author vvt
 */
public class Client implements Serializable{
    private int id;
    private String name, address, mail, tel, note;

    public Client() {
        super();
    }

    public Client(int id, String name, String address, String mail, String tel, String note) {
        super();
        this.name = name;
        this.address = address;
        this.mail = mail;
        this.tel = tel;
        this.note = note;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
    
}
